    private void centralizarPanel(JPanel p) {
        int pontoLargura = ((this.getWidth() / 2) - p.getWidth() / 2);
        int pontoAltura = ((this.getHeight() / 2) - p.getHeight() / 2 - 30);

        p.setLocation(pontoLargura, pontoAltura);
    }

    private void setValoresDosCampos(Candidato c) {
        if (c == null) {
            this.tfNome.setText("Nulo");
            this.tfNumero.setText("Nulo");
        } else {
            this.tfNome.setText(c.getNome());
            this.tfNumero.setText(Integer.toString(c.getNumero()));
            this.lbImagem.setIcon(new ImageIcon(c.getFoto()));
        }

    }
