    private void confirmar(){
//        AudioClip musicaConfirmarEleicao = Applet.newAudioClip(getClass().getResource("/sounds/somComfirmarVotacao.wav"));
//        musicaConfirmarEleicao.play();
        
        
        if (ControleDeVotacao.getCandidatoEscolhido() == null) {
            try {
                ge.AnularVoto(ControleDeVotacao.getEleicaoSelecionada(), ControleDeVotacao.getEleitorIdentificado());
            } catch (ConnectionException | SQLStatementException ex) {
                JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            try {
                ge.votar(ControleDeVotacao.getCandidatoEscolhido(), ControleDeVotacao.getEleitorIdentificado(), ControleDeVotacao.getEleicaoSelecionada(), ControleDeVotacao.getEleitorIdentificado().getVoto());
            } catch (ConnectionException | SQLStatementException ex) {
                JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
            }

        }

        JOptionPane.showMessageDialog(rootPane, "Obrigado por votar!", "Voto confirmado!", JOptionPane.INFORMATION_MESSAGE);

        FrmEscolherEleicao fee = new FrmEscolherEleicao();
        fee.setVisible(true);
        ControleDeVotacao.setCandidatoEscolhido(null);
        ControleDeVotacao.setEleicaoSelecionada(null);
        this.dispose();
    }

   
