    private void tratarExcecoes(ValidarAdministradorException ex) {
        if(((ValidarAdministradorException)ex).getTipe() ==  0){
            this.lbTratarConta.setText("O nome da conta está em branco!");
            this.tfNovaConta.selectAll();
            this.tfNovaConta.requestFocus();
        }else{
            this.lbTratarConta.setText(null);
        }
        
        if(((ValidarAdministradorException)ex).getTipe() ==  1){
            this.lbTratarSenha.setText("A senha possuí muitos caracteres!");
            this.psSenha.selectAll();
            this.psSenha.requestFocus();
        }else{
            this.psSenha.setText(null);
        }
        
        if(((ValidarAdministradorException)ex).getTipe() ==  2){
            this.lbTratarSenha.setText("A senha possuí poucos caracteres!");
            this.psSenha.selectAll();
            this.psSenha.requestFocus();
        }else{
            this.psSenha.setText(null);
        }
    }
