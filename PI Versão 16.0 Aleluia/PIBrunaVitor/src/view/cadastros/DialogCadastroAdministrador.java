    private void btConcluidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConcluidoActionPerformed

        try {
            if (administrador == null) {
                if (new String(getPsNovaSenha().getPassword()).equals(new String(getPsSenha().getPassword()))) {
                    administrador = new Administrador();
                    administrador.setUsuario(this.tfNovaConta.getText());
                    administrador.setSenha(new String(getPsSenha().getPassword()));

                    ga.salvar(administrador);
                    DialogArmazenamentoAdministrador.definirValoresLista(ga.getTodosAdministradores());
                    this.dispose();

                } else {
                    JOptionPane.showMessageDialog(this, "Atenção! As senhas correspondem!", "Atenção!", JOptionPane.WARNING_MESSAGE);
                    this.getPsSenha().selectAll();
                    this.getPsNovaSenha().selectAll();
                    this.getPsSenha().requestFocus();

                }

            } else {
                administrador.setUsuario(this.tfNovaConta.getText());
                administrador.setSenha(new String(getPsSenha().getPassword()));
                ga.atualizar(administrador);
                DialogArmazenamentoAdministrador.definirValoresLista(ga.getTodosAdministradores());
                this.dispose();
            }
        } catch (SQLStatementException | ConnectionException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
        } catch (ValidarAdministradorException ex) {
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
            tratarExcecoes(ex);
        }
    }//GEN-LAST:event_btConcluidoActionPerformed

