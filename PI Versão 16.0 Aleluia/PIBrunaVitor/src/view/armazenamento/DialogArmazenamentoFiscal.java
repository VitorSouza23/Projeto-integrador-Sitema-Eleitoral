    public static void setListaFiscal(java.util.List<Fiscal> listaDados) {
        DialogArmazenamentoFiscal.tFiscaisCadastrados.setModel(new FiscalTableModel(listaDados));
    }

    private void configurarTabela() {
        try {
            DialogArmazenamentoFiscal.setListaFiscal(gf.obterTodos());
        } catch (ConnectionException | SQLStatementException | SQLException | NomeException | ValidarEmailException ex) {
            Logger.getLogger(DialogArmazenamentoEleitor.class.getName()).log(Level.SEVERE, null, ex);
        }
        tFiscaisCadastrados.setAutoCreateColumnsFromModel(false);
        java.awt.FontMetrics fm = tFiscaisCadastrados.getFontMetrics(tFiscaisCadastrados.getFont());
        tFiscaisCadastrados.setColumnModel(new FiscalColumnModel(fm));
        tFiscaisCadastrados.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    public Fiscal getFiscalSelecionado() {
        return ((FiscalTableModel) tFiscaisCadastrados.getModel()).getObjetoSelecionado(
                tFiscaisCadastrados.getSelectedRow());
    }
