    private void btExclauirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExclauirActionPerformed

        try {

            fiscal = new Fiscal();
            fiscal = gf.obterTodos().get(tFiscaisCadastrados.getSelectedRow());
            int opc = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente remover o Fiscal?", "Excluir..", JOptionPane.YES_NO_OPTION);
            if (opc == 0) {
                gf.remover(fiscal);
                configurarTabela();
            }
        } catch (SQLStatementException | ConnectionException | NomeException | SQLException | ValidarEmailException ex) {
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btExclauirActionPerformed

    private void tFiscaisCadastradosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tFiscaisCadastradosMouseClicked
        if (evt.getClickCount() == 2) {
            DialogCadastroDeFiscal dcf = new DialogCadastroDeFiscal(null, rootPaneCheckingEnabled);
            try {
                dcf.setFiscalInterface(gf.obterTodos().get(tFiscaisCadastrados.getSelectedRow()));
                dcf.getTfEmail().setText(dcf.getFiscalInterface().getEmail());
                dcf.getTfEndereco().setText(dcf.getFiscalInterface().getEndereco());
                dcf.getTfNome().setText(dcf.getFiscalInterface().getNome());
                dcf.getTfTelefone().setText(dcf.getFiscalInterface().getTelefone());
                dcf.setVisible(true);
            } catch (SQLStatementException | ConnectionException | NomeException | SQLException | ValidarEmailException ex) {
                JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_tFiscaisCadastradosMouseClicked

