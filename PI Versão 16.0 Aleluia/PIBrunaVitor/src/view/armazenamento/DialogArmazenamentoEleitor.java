    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed

        try {

            eleitor = new Eleitor();
            eleitor = ge.obterTodos().get(tEleitoresCadastrados.getSelectedRow());
            int opc = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente remover o Eleitor?", "Excluir..", JOptionPane.YES_NO_OPTION);
            if (opc == 0) {
                ge.remover(eleitor);
                configurarTabela();
            }
        } catch (SQLStatementException | ConnectionException | NomeException | MatriculaException | ValidarEmailException | ValidarCursoException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro: " + ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btExcluirActionPerformed

    private void tEleitoresCadastradosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tEleitoresCadastradosMouseClicked
        if (evt.getClickCount() == 2) {
            DialogCadastroEleitor dce = new DialogCadastroEleitor(null, rootPaneCheckingEnabled);

            try {
                dce.setEleitorInterface(ge.obterTodos().get(tEleitoresCadastrados.getSelectedRow()));
                dce.getTfCurso().setText(dce.getEleitorInterface().getCurso());
                dce.getTfEmail().setText(dce.getEleitorInterface().getEmail());
                dce.getTfMatricula().setText(dce.getEleitorInterface().getMatricula());
                dce.getTfNome().setText(dce.getEleitorInterface().getNome());
                dce.setVisible(true);
            } catch (SQLStatementException | ConnectionException | NomeException | MatriculaException | ValidarEmailException | ValidarCursoException ex) {
                JOptionPane.showMessageDialog(rootPane, "Erro: " + ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_tEleitoresCadastradosMouseClicked

    private void tfFiltroCursoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfFiltroCursoKeyReleased
        try {
            if (this.tfNomeFiltro.getText().isEmpty()) {
                setListaEleitor(ge.obterTodos(this.tfFiltroCurso.getText()));
            } else {
                setListaEleitor(ge.obterTodosPorNomeECurso(this.tfNomeFiltro.getText(), this.tfFiltroCurso.getText()));
            }

        } catch (SQLStatementException | ConnectionException | NomeException  | MatriculaException | ValidarEmailException | ValidarCursoException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro: " + ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tfFiltroCursoKeyReleased

