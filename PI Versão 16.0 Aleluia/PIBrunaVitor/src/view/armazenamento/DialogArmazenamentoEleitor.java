    public static void setListaEleitor(java.util.List<Eleitor> listaDados) {
        DialogArmazenamentoEleitor.tEleitoresCadastrados.setModel(new EleitorTableModel(listaDados));
    }

    private void configurarTabela() {
        try {
            DialogArmazenamentoEleitor.setListaEleitor(ge.obterTodos());
        } catch (ConnectionException | SQLStatementException | NomeException | MatriculaException | ValidarEmailException | ValidarCursoException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro: " + ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }
        tEleitoresCadastrados.setAutoCreateColumnsFromModel(false);
        java.awt.FontMetrics fm = tEleitoresCadastrados.getFontMetrics(tEleitoresCadastrados.getFont());
        tEleitoresCadastrados.setColumnModel(new EleitorColumnModel(fm));
        tEleitoresCadastrados.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    public Eleitor getEleitorSelecionado() {
        return ((EleitorTableModel) tEleitoresCadastrados.getModel()).getObjetoSelecionado(
                tEleitoresCadastrados.getSelectedRow());
    }

